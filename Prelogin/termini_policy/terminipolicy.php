    <p>Utilizziamo cookie e tecnologie di tracciamento simili per tracciare l'attività sul nostro Servizio e conservare
        determinate informazioni.</p>
    <p>I cookie sono file con una piccola quantità di dati che possono includere un identificatore univoco anonimo. I
        cookie vengono inviati al vostro browser da un sito web e memorizzati sul vostro dispositivo. Altre tecnologie
        di tracciamento utilizzate sono anche beacon, tag e script per raccogliere e tenere traccia delle informazioni e
        per migliorare e analizzare il nostro Servizio.</p>
    <p>Potete chiedere al vostro browser di rifiutare tutti i cookie o di indicare quando viene inviato un cookie.
        Tuttavia, se non si accettano i cookie, potrebbe non essere possibile utilizzare alcune parti del nostro
        Servizio.</p>
    <p>Esempi di cookie che utilizziamo:</p>
    <ul>
        <li><strong>Cookie di sessione.</strong> Utilizziamo i cookie di sessione per gestire il nostro servizio.</li>
        <li><strong>Cookie di preferenza.</strong> Utilizziamo i cookie di preferenza per ricordare le vostre preferenze
            e varie impostazioni.</li>
        <li><strong>Cookie di sicurezza.</strong> Utilizziamo i cookie di sicurezza per motivi di sicurezza.</li>
